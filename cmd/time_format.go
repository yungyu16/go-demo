package main

import (
	"fmt"
	"time"
)

func main() {
	// 使用一个固定的时间作为测试基准（避免受系统时间影响）
	testTime := time.Date(2023, time.November, 15, 14, 30, 45, 123456789, time.UTC)
	fmt.Println("测试基准时间（UTC）:", testTime)
	fmt.Println("------------------------------------------------")

	// 定义各种时间格式模板和说明
	formats := []struct {
		format string
		desc   string
	}{
		{"2006-01-02", "年-月-日（数字，补零）"},
		{"2006/01/02", "年/月/日（数字，补零）"},
		{"02-01-2006", "日-月-年（数字，补零）"},
		{"2006年01月02日", "年-月-日（带中文单位）"},
		{"15:04:05", "时:分:秒（24小时制，补零）"},
		{"3:04:05 PM", "时:分:秒（12小时制，带AM/PM）"},
		{"2006-01-02 15:04:05", "年月日 时分秒（24小时制）"},
		{"2006-01-02T15:04:05Z", "ISO 8601格式（UTC）"},
		{"Mon Jan 02 15:04:05 2006", "星期 月 日 时分秒 年"},
		{"20060102150405", "紧凑的年月日时分秒（无分隔符）"},
		{"2006-01-02 15:04:05.999", "带毫秒的时间格式"},
		{"2006-01-02 15:04:05.999999999", "带纳秒的时间格式"},
		{"2006-01-02 15:04:05 MST", "带时区缩写的时间格式"},
		{"2006-01-02 15:04:05 -0700", "带时区偏移的时间格式"},
		{"06/01/02 3:04 PM", "简写年份 月/日 12小时制时间"},
	}

	// 打印各种格式的结果
	for i, f := range formats {
		result := testTime.Format(f.format)
		fmt.Printf("%d. %-30s 格式: %-30s 结果: %s\n", i+1, f.desc, f.format, result)
	}

	fmt.Println("\n------------------------------------------------")
	fmt.Println("注意：Go的时间格式化必须使用参考时间'2006-01-02 15:04:05'作为模板")
	fmt.Println("参考时间的含义：2006年（年）、01月（月）、02日（日）、15时（24小时制）、04分、05秒")
	fmt.Println("其他可用元素：06（简写年）、1（12小时制时）、Mon（星期缩写）、Jan（月份缩写）、MST（时区）等")
}
